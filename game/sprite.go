// advanceAnimation toggles animated maze codes by one step. Call this once
// per game tick (player step or equivalent action) to drive all sprite
// animation at the player-movement cadence.
func advanceAnimation(m *maze.Maze) {
	area := m.Map()
	pos := m.PlayerPos - 2*maze.MazeColumns - 2
	for c := 0; c < 5; c++ {
		for r := 0; r < 4; r++ {
			index := pos + r*maze.MazeColumns + c
			switch area[index] {
			case maze.Prisoner, maze.Prisoner2,
				maze.Treasure, maze.Treasure2,
				maze.Mazog, maze.Mazog2:
				area[index] += 0x80
			}
		}
	}
}

// showSprites displays a screenful of sprites around the player location.
// A maximum of num columns of sprites are displayed. Does not advance
// animation state — call advanceAnimation first when a tick has occurred.
func showSprites(m *maze.Maze, num int) {
	area := m.Map()
	pos := m.PlayerPos - 2*maze.MazeColumns - 2

	// loop for each sprite column within the maze to render
	for c := 0; c < num; c++ {
		// loop to render 4 maze codes vertically
		for r := 0; r < 4; r++ {
			index := pos + r*maze.MazeColumns + c
			code := area[index]
			sp, ok := sprites[code]
			if ok {
				sp.render(r, c)
			}
		}
	}
}

type sprite [30]byte

func (sp sprite) render(r, c int) {
	row := 3 + r*5
	col := 1 + c*6
	index := 0
	for i := 0; i < 5; i++ {
		for j := 0; j < 6; j++ {
			graphics.PutZXChar(row+i, col+j, sp[index])
			index++
		}
	}
}

// renderWallBackground fills the entire 5×4 sprite grid with InternalWall tiles,
// giving a clean wall background independent of the current maze state.
func renderWallBackground() {
	wall := sprites[maze.InternalWall]
	for r := 0; r < 4; r++ {
		for c := 0; c < 5; c++ {
			wall.render(r, c)
		}
	}
}

