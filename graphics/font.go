package graphics

var font = map[byte][8]byte{
	0x00: {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // space
	0x01: {0xf0, 0xf0, 0xf0, 0xf0, 0x00, 0x00, 0x00, 0x00}, // upper-left block
	0x02: {0x0f, 0x0f, 0x0f, 0x0f, 0x00, 0x00, 0x00, 0x00}, // upper-right block
	0x03: {0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00}, // upper block
	0x04: {0x00, 0x00, 0x00, 0x00, 0xf0, 0xf0, 0xf0, 0xf0}, // lower-left block
	0x05: {0xf0, 0xf0, 0xf0, 0xf0, 0xf0, 0xf0, 0xf0, 0xf0}, // left block
	0x06: {0x0f, 0x0f, 0x0f, 0x0f, 0xf0, 0xf0, 0xf0, 0xf0}, // lower-left upper-right block
	0x07: {0xff, 0xff, 0xff, 0xff, 0xf0, 0xf0, 0xf0, 0xf0}, // inverse lower-right block
	0x08: {0xaa, 0x55, 0xaa, 0x55, 0xaa, 0x55, 0xaa, 0x55}, // checkerboard
	0x09: {0x00, 0x00, 0x00, 0x00, 0xaa, 0x55, 0xaa, 0x55}, // lower checkerboard
	0x0a: {0xaa, 0x55, 0xaa, 0x55, 0x00, 0x00, 0x00, 0x00}, // upper checkerboard
	0x0b: {0x00, 0x24, 0x24, 0x00, 0x00, 0x00, 0x00, 0x00}, // "
	0x0c: {0x00, 0x1c, 0x22, 0x78, 0x20, 0x20, 0x7e, 0x00}, // pound
	0x0d: {0x00, 0x08, 0x3e, 0x28, 0x3e, 0x0a, 0x3e, 0x08}, // $
	0x0e: {0x00, 0x00, 0x00, 0x08, 0x00, 0x00, 0x08, 0x00}, // :
	0x0f: {0x00, 0x3c, 0x42, 0x04, 0x08, 0x00, 0x08, 0x00}, // ?
}

func init() {
	for i := 0; i < 0x10; i++ {
		var c [8]byte
		for j := 0; j < 8; j++ {
			c[j] = ^font[byte(i)][j]
		}
		font[byte(i)+0x80] = c
	}
}
